#define DEFAULT_FRAME_TIME 250 //42 is close to 24fps
#define DEFAULT_BRIGHTNESS 15
#define FULL_BRIGHTNESS 15
#define DISPLAY_COLON 0b01000000
#define LAST_FRAME 0b10000000
#define RANDOMIZE_DISPLAY 0b00000001
#define NO_FLAGS 0b00000000

typedef struct {
  uint8_t digitMasks[4];      // Segment index 2 is the colon ':' on this display
  unsigned long holdTime; // in milliseconds
  uint8_t brightness;     // value of 1 - 15
  uint8_t controlBits;
} AnimationFrame;

// Description:
//
//  HEX codes 7 segment
//
//          01
//      ---------
//     /         /
//  20 /         / 02
//     /         /
//     /--- 40 --/
//     /         /
//  10 /         / 04
//     /         /
//      ---------
//         08
//
// When RANDOMIZE_DISPLAY is enabled for a frame, all four digits are randomized and &'d with the digitMask (bitwise).
//  This way we can control which segments are being randomized.
//
// Template
// All Off
//{ { 0b00000000, 0b00000000, 0b00000000, 0b00000000 }, DEFAULT_FRAME_TIME, DEFAULT_BRIGHTNESS,  LAST_FRAME | DISPLAY_COLON | RANDOMIZE_DISPLAY},
// All On
//{ { 0b11111111, 0b11111111, 0b11111111, 0b11111111 }, DEFAULT_FRAME_TIME, DEFAULT_BRIGHTNESS,  LAST_FRAME | DISPLAY_COLON | RANDOMIZE_DISPLAY},

